var jump = function(nums) {
  if (nums.length <= 2) return nums.length - 1
  let start = 0,
    end = 0,
    steps = 0
  while (end < nums.length - 1) {
    let preEnd = end
    for (let i = start; i <= preEnd; i++) {
      end = Math.max(nums[i] + i, end)
    }
    start = preEnd + 1
    steps++
  }
  return steps
}
