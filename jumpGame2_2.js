  var getMinSteps = (start = 0) => {
    let arr = nums.slice(start)
    let n = arr[0] || 1
    if (arr.length - 1 <= n) {
      steps[start] = 1
      return 1
    } else {
      if (n === 1) {
        if (steps[start + 1]) {
          return steps[start + 1] + 1
        }
        return getMinSteps(start + 1) + 1
      }
      let a = []
      while (n >= 1) {
        let i = n--
        if (steps[start + i]) {
          a.push(steps[start + i] + 1)
        } else {
          a.push(getMinSteps(start + i) + 1)
        }
      }
      steps[start] = Math.min(...a)
      return steps[start]
    }
  }
  return getMinSteps()
